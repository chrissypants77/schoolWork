import math
def area_of_rectangle(length, width):
    return length * width

def scope_of_rectangle(length, width):
    return length*2 + width*2

def area_of_circle(radius):
    return math.pi * radius**2

def scope_of_circle(radius):
    return 2 * math.pi * radius
